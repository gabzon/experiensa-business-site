      <div class="tab-pane fade show active" id="v-pills-title" role="tabpanel" aria-labelledby="v-pills-title-tab">
        The title is the most important word (or sentence) of the offer since it is used as keywords for: <br>
        <ol>
          <li>Attracting and engaging the user to click for more information</li>
          <li>SEO Purposes</li>
        </ol>

        Some brochures uses a title for the offer, some others don't. Therefore, before you start typing the title you need to determine the type of offer it is
        <ul>
          <li>Only Flight</li>
          <li>Flight + Hotel</li>
          <li>only Hotel</li>
          <li>Circuit (many places/countries)</li>
          <li>Cruise (many places/countries)</li>
          <li>Other</li>
        </ul>

        Use the title of the PDF File or Brochure <b>IF</b> the title is simple and attractive, otherwise if the offer has no title, or the title is long and complicated then create a new title, you have the following options: <br>
        <ul>
          <li>Offer has 1 city: Use the name of the city</li>
          <li>Many city same country: Use the name of the Country</li>
          <li>Many cities many countries: Use the name of the region/sub-region (ex: Scandinavia/Caribbean, etc.)</li>
          <li>If it is ClubMed, then title is: ClubMed + their title</li>
          <li>If it is a Circuit you can name it: Circuit + name of the country/region</li>
          <li>If it is a hotel: Name of Hotel + City + Stars</li>
        </ul>
        <i>Advice: Think about movie titles, they need to be short and clear</i>
      </div>
      <div class="tab-pane fade" id="v-pills-description" role="tabpanel" aria-labelledby="v-pills-description-tab">
        It describes what the offer is about. Depending on the type of offer you will find a description or not. Examples of description would be an itinerary or what you will do there, reasons for you to go there, etc
        <br><br>
        <i>*Do not confuse a description with what is included or excluded</i>
        <br><br>
        If an offer has a description, copy and paste the description of the offer from the <i>PDF File</i> or <i>website</i> into the corresponding T.O. Experiensa Website.
      </div>
      <div class="tab-pane fade" id="v-pills-price" role="tabpanel" aria-labelledby="v-pills-price-tab">
        Input the regular price of the offer. If the offers has many prices according to dates and availability, input the cheapest price and check the “starting from” checkbox.
        <br><br>
        New rule since september 2018: <i>Always check the “starting from” checkbox</i>
        <br>
        <br>
        If the offer has many prices according to options and dates, copy the rest of the prices in the description section
      </div>
      <div class="tab-pane fade" id="v-pills-dates" role="tabpanel" aria-labelledby="v-pills-dates-tab">
        Dates show the validity of the offers and it indicates the price according to a season or period in the offer. Some offers give you dates, others don't.<br><br>
        It is <b class="ttu">important</b> for us to input dates even if the offer has none, it allows us to create filters according to an specific season or period in time. Therefore, dates are mandatory.
        <br>
        <br>
        <ul>
          <li>If available add the starting date and end date</li>
          <li>If you have many dates and/or options dates
            <ul>
              <li>Add the first date of the available options in the Start Date field</li>
              <li>Add the last date of the available options in the End Date field</li>
              <li>All dates and option will be explain in the description text</li>
            </ul>
          </li>
          <li>If the offer has no dates, add current day (today) in the starting date and calculate 6 months after for the end of the offer</li>
        </ul>

      </div>
