      <div class="tab-pane fade" id="v-pills-included" role="tabpanel" aria-labelledby="v-pills-included-tab">
        This fields describes what does the offers includes, it answers the question: What do I get for the money I pay? This field is Mandatory. All offers need to have an included section <b>ALWAYS</b>
        <br><br>
        <i>Be careful: Some offers show the offer section at the bottom of the page written very small</i>
        <br>
        <br>
        Do not confuse this field with the description field. Included information should not be copied in the description section but in the included fields, at the right side of the admin<br>
        <br>
        Included example: Car <br>
        Description example: Very beautiful red car with leather seats, 0 km, new, Audi or Mercedez Benz, vertical
        <br>
        <br>
        what does it include?
        <ul>
          <li>Flight</li>
          <li>Hotel</li>
          <li>Taxes</li>
          <li>Guide</li>
          <li>Car</li>
          <li>etc</li>
        </ul>

      </div>
