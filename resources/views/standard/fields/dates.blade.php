Dates show the validity of the offers and it indicates the price according to a season or period in the offer. Some offers give you dates, others don't.<br><br>
It is <b class="ttu">important</b> for us to input dates even if the offer has none, it allows us to create filters according to an specific season or period in time. Therefore, dates are mandatory.
<br>
<br>
<ul>
  <li>If available add the starting date and end date</li>
  <li>If you have many dates and/or options dates
    <ul>
      <li>Add the first date of the available options in the Start Date field</li>
      <li>Add the last date of the available options in the End Date field</li>
      <li>All dates and option will be explain in the description text</li>
    </ul>
  </li>
  <li>If the offer has no dates, add current day (today) in the starting date and calculate 6 months after for the end of the offer</li>
</ul>

