The title is the most important word (or sentence) of the offer since it is used as keywords for: <br>
<ol>
  <li>Main title of the offer</li>
  <li>Attracting and engaging the user to click for more information</li>
  <li>SEO Purposes</li>
</ol>

Some brochures uses a title for the offer, some others don't. Therefore, before you start typing the title you need to determine the type of offer it is
<ul>
  <li>Only Flight</li>
  <li>Flight + Hotel</li>
  <li>only Hotel</li>
  <li>Circuit (many places/countries)</li>
  <li>Cruise (many places/countries)</li>
  <li>Other</li>
</ul>

Use the title of the PDF File or Brochure <b>IF</b> the title is simple and attractive, otherwise if the offer has no title, or the title is long and complicated then create a new title, you have the following options: <br>
<ul>
  <li>Offer has 1 city: Use the name of the city</li>
  <li>Many city same country: Use the name of the Country</li>
  <li>Many cities many countries: Use the name of the region/sub-region (ex: Scandinavia/Caribbean, etc.)</li>
  <li>If it is ClubMed, then title is: ClubMed + their title</li>
  <li>If it is a Circuit you can name it: Circuit + name of the country/region</li>
  <li>If it is a hotel: Name of Hotel + City + Stars</li>
</ul>
<i>Advice: Think about movie titles, they need to be short and clear</i>
