This field is equivalent to hashtags in social media. You use it as keywords for researching
<br><br>
It is important to write tags (or keywords) cuz this keywords are linked to the search field.
Example imagine you put a trip to Cuba, and in the description it is written there is a salsa party.
<br><br>
When you put the tags "salsa" & "party" in the keywords they will be associated. So if someone puts in the search field salsa party it will appear.
