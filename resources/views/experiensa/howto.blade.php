<h3>How to input the information in Experiensa?</h3>
<p>Read carefully each travel offer and input the offer data into the corresponding field in the Tour Operator’s website. Each voyage created needs to have the following information:</p>
<div class="row">
  <div class="col-1">
    <strong>Title</strong>
  </div>
  <div class="col-11 mb-3">
    Use the title of the PDF File or Brochure.
    If the offer has no title, you have the following options:

    Offer has 1 city: Use the name of the city
    Many city same country: Use the name of the Country
    Many cities many countries: Use the name of the region/sub-region (ex: Scandinavia/Caribbean, etc.)
  </div>
  <div class="col-1">
    <span class="b">Description</span>
  </div>
  <div class="col-11 mb-3">
    Copy and paste the description of the offer from the <i>PDF File</i> or <i>website</i> into the corresponding T.O. Experiensa Website.
  </div>
  <div class="col-1">
    <b>Price</b>
  </div>
  <div class="col-11 mb-3">
    Input the regular price of the offer. If the offers has many prices according to dates and availability, input the cheapest price and check the “starting from” checkbox. (<i>Always check the “starting from” checkbox</i>)
  </div>
  <div class="col-1">
    <b>Dates</b>
  </div>
  <div class="col-11">
    - If available add the starting date and end date.<br>
    - If you have many dates and/or options dates
    <ul>
      <li>Add the first date of the available options in the Start Date field</li>
      <li>Add the last date of the available options in the End Date field</li>
      <li>All dates and option will be explain in the description text</li>
    </ul>
  </div>
  <div class="col-1">
    <b>World region</b>
  </div>
  <div class="col-11 mb-3">
    World region correspond to the continent or part of the world the destination country(s) is found. (Ex: North America, South America, Caribbeans, Scandinavia, Indian Ocean, etc)
  </div>
  <div class="col-1">
    <strong>City/Places</strong>
  </div>
  <div class="col-11 mb-3">
    Add the city or place in the destination box at the right of the screen. It is called destination because sometime it is difficult to define what it is (Ex: Gran Sabana in Venezuela) is not a city, and not really a place)
  </div>
  <div class="col-1">
    <b>Included</b>
  </div>
  <div class="col-11">
    Included information should not be in the description text but in the included fields (right side)
  </div>
  <div class="col-1">
    <b>Excluded</b>
  </div>
  <div class="col-11">
    Excluded information should not be in the description text but in the excluded fields (right side)
  </div>
  <div class="col-1">
    <b>Photo Gallery</b>
  </div>
  <div class="col-11">
    – Ask Gabriel for missing photos, He is responsible for collecting and optimizing photos for the experiensa system.<br>
    – If Gabriel is unable to get the photos, find yourself images on internet. <br>
    - IMPORTANT the images need to be free of copyrights. <br>
    - The website where you find the image should mention free copyright for commercial use. (Copy the link of the website and keep it save)<br>
    – Check on free websites like:
    <ul>
      <li>https://unsplash.com/</li>
      <li>https://pixabay.com/</li>
    </ul>
    – Request images and rights to Official Tourist office of the place by email (send the images to Gabriel to be optimized)
  </div>
</div>
